/// # Sanity Checks.
const _: () = {
	assert!(
		(1_usize << CodeSize::MAX as u8) == DICTIONARY_SIZE &&
		DICTIONARY_SIZE - 1 == Code::MAX as usize,
		"BUG: dictionary constants are wrong.",
	);
	assert!(
		1 < MIN_NONGREEDY_MATCH.get(),
		"BUG: non-greedy match length must be more than one.",
	);

	// Double-check u8->Code alignment.
	let mut i = u8::MIN;
	loop {
		assert!(
			(Code::from_u8(i) as u16) == (i as u16),
			"BUG: Code::from_u8 is wonky.",
		);
		if i == u8::MAX { break; }
		i += 1;
	}
};



/// # LZW Encode Frame Indices.
///
/// This method encodes the image data for a single GIF frame, hopefully
/// improving upon the "simpler" result produced by the `gif` encoder itself.
///
/// `None` is returned if there are any problems.
pub(super) fn encode_frame(src: &[u8], alignment: NonZeroUsize) -> Option<Vec<u8>> {
	let mut encoder = LzwEncoder::new(src, alignment);

	// Crunch aligned sub-slices of the data to build up a sense of the "best"
	// breakpoints.
	for i in (0..src.len()).rev() {
		if ! i.is_multiple_of(alignment.get()) { continue; }

		// Estimate cost.
		encoder.optimize_partial(i, 0, None)?;

		// Repeat non-greedy.
		if ! encoder.greedy() {
			encoder.set_greedy(true);
			encoder.optimize_partial(i, 0, None)?;
			encoder.set_greedy(false);
		}
	}

	// One more time around to collect the results.
	let out = encoder.optimize()?;

	// Return the compressed data if any.
	if out.len() < 2 { None }
	else { Some(out) }
}



