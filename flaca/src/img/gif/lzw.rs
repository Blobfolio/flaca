#[derive(Debug)]
/// # Little Endian Bit Writer.
struct LsbBitWriter {
	/// # Internal Buffer.
	buffer: u64,

	/// # Bits in Buffer.
	buffer_bits: u8,

	/// # Bytes Written.
	done: Vec<u8>,
}

impl LsbBitWriter {
	#[must_use]
	/// # New.
	///
	/// Initialize and return a new LSB writer with `capacity` pre-reserved.
	fn new(min_code_size: CodeSize, capacity: usize) -> Self {
		let mut done = Vec::with_capacity(capacity);
		done.push(min_code_size as u8);
		Self {
			buffer: 0,
			buffer_bits: 0,
			done,
		}
	}

	/// # Write Bits.
	fn write_bits(&mut self, code: Code, bits: CodeSize) {
		// Shift code into buffer at the current bit position.
		self.buffer |= (code as u64) << self.buffer_bits;
		self.buffer_bits += bits as u8;

		// Flush any complete bytes.
		while 8 <= self.buffer_bits {
			self.done.push((self.buffer & 0xFF) as u8);
			self.buffer >>= 8;
			self.buffer_bits -= 8;
		}
	}

	#[must_use]
	/// # Flush and Return.
	fn finish(mut self) -> Vec<u8> {
		// Flush the remaining bits, if any, before we go.
		if 0 != self.buffer_bits {
			self.done.push((self.buffer & 0xFF) as u8);
		}

		self.done
	}
}



#[derive(Debug, Clone, Copy)]
/// # Best Block Stats.
///
/// This struct holds a summary of a single LZW block configuration.
struct BestBlock {
	/// # Total Bits.
	bits: usize,

	/// # Total Bytes.
	bytes: usize,

	/// # Any Non-Greedy Matches?
	nongreedy: bool,
}

impl BestBlock {
	#[must_use]
	/// # New.
	const fn new() -> Self {
		Self {
			bits: 0,
			bytes: 0,
			nongreedy: false,
		}
	}
}

#[derive(Debug)]
/// # Best Blocks.
///
/// This struct holds summaries of various LZW block configurations.
struct BestBlocks(HashMap<usize, BestBlock, NoHash>);

impl BestBlocks {
	#[must_use]
	/// # New.
	fn new(capacity: usize) -> Self {
		Self(HashMap::with_capacity_and_hasher(capacity, NoHash::default()))
	}

	#[must_use]
	/// # Get Mut.
	///
	/// Return a mutable reference to the underlying stats, creating them
	/// on-the-fly if they don't already exist.
	fn get_mut(&mut self, idx: usize) -> &mut BestBlock {
		self.0.entry(idx).or_insert_with(BestBlock::new)
	}

	#[must_use]
	/// # Is Empty?
	fn is_empty(&self) -> bool { self.0.is_empty() }

	#[must_use]
	/// # Best Bits.
	///
	/// Return the number of bits for a best.
	fn bits(&self, idx: usize) -> Option<NonZeroUsize> {
		self.0.get(&idx).and_then(|v| NonZeroUsize::new(v.bits))
	}

	#[must_use]
	/// # Best Bytes.
	///
	/// Return the number of bytes for a best.
	fn bytes(&self, idx: usize) -> Option<NonZeroUsize> {
		self.0.get(&idx).and_then(|v| NonZeroUsize::new(v.bytes))
	}

	#[must_use]
	/// # Greedy-Only.
	///
	/// Returns `true` if the best is non-empty without any non-greedy wins.
	fn greedy(&self, idx: usize) -> bool {
		self.0.get(&idx).is_some_and(|v| ! v.nongreedy && 0 != v.bytes)
	}

	#[must_use]
	/// # Non-Greedy Wins?
	///
	/// Returns `true` if the best is non-empty and has any non-greedy wins.
	fn nongreedy(&self, idx: usize) -> bool {
		self.0.get(&idx).is_some_and(|v| v.nongreedy)
	}
}



/// # Dictionary Inner.
type DictionaryInner = [[Option<Code>; MAX_COLOR_TABLE]; DICTIONARY_SIZE];

#[derive(Debug, Clone)]
/// # Dictionary.
struct Dictionary {
	/// # Entries.
	inner: Box<DictionaryInner>,

	/// # Current Idx.
	idx: Code,
}

impl Default for Dictionary {
	/// # New.
	///
	/// This tree isn't quite as bad as some of the Zopfli ones, but it
	/// still needs to be boxed to keep it off the stack.
	fn default() -> Self {
		// Vec->Box prevents intermediary heap allocation.
		let inner: Box<DictionaryInner> = vec![[None; MAX_COLOR_TABLE]; DICTIONARY_SIZE]
			.try_into()
			.unwrap();
		Self {
			inner,
			idx: Code::MIN,
		}
	}
}

impl Dictionary {
	/// # Reset Dictionary.
	///
	/// Reset all code bytes to `None`, and the starting index to `idx`.
	fn reset(&mut self, idx: Code) {
		self.inner.as_flattened_mut().fill(None);
		self.idx = idx;
	}

	/// # Set Last Byte.
	///
	/// Set the last code byte if unknown, and increment the current index.
	const fn set_last_byte(&mut self, code: Code, byte: u8) {
		if let Some(next_idx) = self.idx.next() {
			if self.get_code_byte(code, byte).is_none() {
				self.inner[code as usize][byte as usize] = Some(self.idx);
			}
			self.idx = next_idx;
		}
	}

	#[must_use]
	/// # Get Code Byte Count.
	const fn get_code_byte(&self, code: Code, byte: u8) -> Option<Code> {
		self.inner[code as usize][byte as usize]
	}

	#[must_use]
	/// # Is Full?
	///
	/// Returns `true` if the current index is the largest possible.
	const fn is_full(&self) -> bool { self.idx.is_max() }

	#[must_use]
	/// # Current Index.
	const fn idx(&self) -> Code { self.idx }

	#[must_use]
	/// # A New Power of Two?
	///
	/// Returns `true` if the current index follows a power-of-two, e.g. 3,
	/// 5, 9.
	const fn new_pow2(&self) -> bool {
		if let Some(prev) = self.idx.prev() {
			(prev as u16).is_power_of_two()
		}
		else { false }
	}
}



/// # LZW Encoder.
struct LzwEncoder<'a> {
	/// # Data.
	data: &'a [u8],

	/// # Alignment.
	alignment: NonZeroUsize,

	/// # Min Code Size.
	min_code_size: CodeSize,

	/// # Best Block(s).
	best: BestBlocks,

	/// # Dictionary.
	dictionary: Dictionary,

	/// # Flags.
	flags: u8,
}

/// # Helper: Flags.
macro_rules! flags {
	( $( $k:ident $v:literal $get:ident $set:ident, )+ ) => (
		#[expect(dead_code, reason = "Macro made me do it.")]
		impl LzwEncoder<'_> {
			$(
				/// # Flag.
				const $k: u8 = $v;

				/// # Get Flag.
				const fn $get(&self) -> bool { Self::$k == self.flags & Self::$k }

				/// # Set Flag.
				const fn $set(&mut self, v: bool) {
					if v { self.flags |= Self::$k; }
					else { self.flags &= ! Self::$k; }
				}
			)+
		}
	);
}

flags! {
	GREEDY                0b0001 greedy                set_greedy,
	READ_ONLY_BEST        0b0010 read_only_best        set_read_only_best,
	AVOID_NONGREEDY_AGAIN 0b0100 avoid_nongreedy_again set_avoid_nongreedy_again,
}

impl<'a> LzwEncoder<'a> {
	#[must_use]
	/// # New.
	fn new(data: &'a [u8], alignment: NonZeroUsize) -> Self {
		let min_code_size = min_code_size(data);
		let best = BestBlocks::new(data.len() / alignment.get() + 1 + 1);
		Self {
			data,
			alignment,
			min_code_size,
			best,
			dictionary: Dictionary::default(),
			flags: Self::AVOID_NONGREEDY_AGAIN,
		}
	}
}

impl LzwEncoder<'_> {
	#[must_use]
	/// # Add Code.
	///
	/// Add/update a code in the dictionary and bump the size counter. The
	/// final code is returned.
	fn add_code(&mut self, from: usize, len: usize) -> Option<Code> {
		let to = from + len;
		let slice = self.data.get(from..to)?;
		let mut bytes = slice.iter().copied();

		// First literal.
		let mut code = Code::from_u8(bytes.next()?);

		// Walk through the dictionary to find the code matching without the
		// last byte.
		for byte in bytes { code = self.dictionary.get_code_byte(code, byte)?; }

		// Last byte.
		if let Some(byte) = self.data.get(to).copied() {
			self.dictionary.set_last_byte(code, byte);
		}

		Some(code)
	}

	#[must_use]
	/// # Clear Code.
	const fn clear_code(&self) -> Code {
		Code::saturating_from(1_u16 << (self.min_code_size as u8))
	}

	#[must_use]
	/// # End of Image Code.
	const fn eoi_code(&self) -> Code {
		Code::saturating_from((1_u16 << (self.min_code_size as u8)) + 1)
	}

	#[must_use]
	/// # Find Match.
	///
	/// Find and return the longest match length for a subslice, or `None` if
	/// none.
	fn find_match(&self, from: usize, max_len: usize) -> Option<usize> {
		let slice = self.data.get(from..from + max_len)?;
		let mut bytes = slice.iter().copied();

		// There is alwayz an LZW code for the first byte.
		let mut code = Code::from_u8(bytes.next()?);

		let mut len = 0;
		for byte in bytes {
			len += 1;
			code = match self.dictionary.get_code_byte(code, byte) {
				Some(v) => v,
				None => return Some(len),
			};
		}

		// Made it all the way!
		Some(max_len)
	}

	#[must_use]
	/// # Find Non-Greedy.
	fn find_nongreedy_match(
		&self,
		from: usize,
		match_len: usize,
		remaining: usize,
	) -> Option<usize> {
		if remaining < match_len { return None; }

		// Next greedy match.
		let mut best = match_len + self.find_match(from + match_len, remaining - match_len)?;
		let target = best + MIN_IMPROVEMENT.get();

		// Check increasingly shorter distances for better matches.
		let mut distance = match_len;
		for i in (1..match_len).rev() {
			let maybe = i + self.find_match(from + i, remaining - i)?;
			if target <= maybe && best < maybe {
				best = maybe;
				distance = i;
			}
		}

		// Found a closer one.
		if distance < match_len { Some(distance) }
		else { None }
	}

	#[must_use]
	/// # Partial Optimization.
	///
	/// Calculate the LZW encoding cost for a subslice of the data, returning
	/// the total data that would be written.
	///
	/// Unless `writer` is passed, the actual data is discarded after the run.
	fn optimize_partial(
		&mut self,
		from: usize,
		max_len: usize,
		mut writer: Option<&mut LsbBitWriter>,
	) -> Option<CodeSize> {
		// Length of current block.
		let mut len = self.data.len().checked_sub(from)?;
		if max_len < len && max_len != 0 { len = max_len; }

		// Current best index.
		let best_idx = from / self.alignment.get();

		// Short-circuit: skip the greedy/non-greedy pass if the last go-around
		// was unsuccessful… unless we're saving the block this time.
		if
			self.greedy() &&
			self.avoid_nongreedy_again() &&
			writer.is_none() &&
			self.best.greedy(best_idx)
		{
			return Some(self.dictionary.idx().min_code_size());
		}

		// Initialization.
		self.dictionary.reset(Code::saturating_from((1_u16 << (self.min_code_size as u8)) + 2));
		let mut state = PartialOptimizationState::new(self.dictionary.idx().min_code_size());
		let mut match_len = 0;

		// Process input.
		for i in from..from + len {
			// We haven't technically processed this byte yet, but by the time
			// we reference this value we will have. (Bumping it early protects
			// against `continue` jumps.
			state.bytes += 1;

			// New match length?
			if match_len == 0 {
				// Can't do anything else.
				if self.dictionary.is_full() || state.tokens_full() { break; }

				let remaining = from + len - i;
				match_len = self.find_match(i, remaining)?;

				// Non-greedy lookahead.
				if
					! self.greedy() &&
					MIN_NONGREEDY_MATCH.get() <= match_len &&
					i + match_len + 4 < self.data.len() &&
					! self.data.get(i..i + match_len)?.array_windows::<2>().all(|[a, b]| a == b) &&
					let Some(next_match_len) = self.find_nongreedy_match(i, match_len, remaining)
				{
					match_len = next_match_len;
					state.nongreedy = true;
				} // Non-greedy.

				// Bump the code size.
				if
					! self.dictionary.is_full() &&
					self.dictionary.new_pow2() &&
					let Some(next_code_size) = state.code_size.next()
				{
					state.code_size = next_code_size;
				}

				// Record it!
				let code = self.add_code(i, match_len)?;
				if let Some(w) = &mut writer { w.write_bits(code, state.code_size); }
				state.bits += state.code_size as usize;
				state.tokens += 1;
			} // New match.

			// We've looked at the last byte; decrease the length for the next
			// go-around.
			match_len = match_len.saturating_sub(1);

			// If we're not building up the blocks on this pass, we can jump
			// back to the top.
			if self.read_only_best() { continue; }

			// If there were no optimizations for the current block, skip it.
			let next_idx =
				if 1 < self.alignment.get() { (i + 1).div_ceil(self.alignment.get()) }
				else { i + 1 };
			if
				i + 1 < self.data.len() &&
				(
					(
						1 < self.alignment.get() &&
						! state.bytes.is_multiple_of(self.alignment.get())
					) ||
					self.best.bits(next_idx).is_none()
				)
			{
				continue;
			}

			// Compute final cost.
			let bits =
				state.true_bits(self.dictionary.new_pow2()) +
				self.best.bits(next_idx).map_or(0, NonZeroUsize::get);

			// Record for posterity if we have a new best.
			let best = self.best.get_mut(best_idx);
			if 0 == best.bits || bits <= best.bits {
				best.bits = bits;
				best.bytes = state.bytes;
				best.nongreedy = state.nongreedy;
			}
		} // Loop.

		// Return the final code size.
		if writer.is_some() { self.dictionary.idx().prev().map(Code::min_code_size) }
		else { Some(state.code_size) }
	}

	#[must_use]
	/// # Optimize.
	///
	/// Optimize and return an LZW-encoded image frame — with leading min code
	/// size byte — or `None` if any issues are encountered.
	fn optimize(&mut self) -> Option<Vec<u8>> {
		// Find the best block restarts.
		let mut restarts = Vec::new();
		let mut pos = 0;
		let mut aligned = 0;
		while pos < self.data.len() {
			// If zero, there was no continuation.
			let len = self.best.bytes(aligned)?;

			pos += len.get();
			aligned = pos / self.alignment.get();
			restarts.push(pos);
		}

		// Bail if empty.
		if restarts.is_empty() { return Some(Vec::new()); }

		// If we fall short of the end, push one last restart.
		if restarts[restarts.len() - 1] < self.data.len() {
			restarts.push(self.data.len());
		}

		// We can finally start writing shit down!
		let mut writer = LsbBitWriter::new(
			self.min_code_size,
			self.data.len() * 3 / 8
		);

		// The first clear code has a width one higher than the min_code_size.
		writer.write_bits(self.clear_code(), self.min_code_size.next()?);

		// Rerun optimizations for each block, adding them to the list.
		let mut pos = 0;
		for (i, restart) in restarts.iter().copied().enumerate() {
			// Dummy entry at zero.
			if restart == 0 { continue; }

			// Block length.
			let len = restart - pos;

			// Switch to faster settings.
			if self.best.is_empty() { std::hint::cold_path() }
			else { self.set_greedy(! self.best.nongreedy(pos / self.alignment.get())); }
			self.set_read_only_best(true);

			// Compute the current block.
			let code_size = self.optimize_partial(pos, len, Some(&mut writer))?;

			// Write a clear or EOI code.
			writer.write_bits(
				if i + 1 == restarts.len() { self.eoi_code() }
				else { self.clear_code() },
				code_size,
			);

			// One more time around.
			pos = restart;
		}

		// Done!
		Some(writer.finish())
	}
}



#[derive(Debug, Clone, Copy)]
/// # Partial Optimization State.
///
/// This struct holds various counts related to greedy/non-greedy calculations.
struct PartialOptimizationState {
	/// # Bits.
	bits: usize,

	/// # Bytes.
	bytes: usize,

	/// # Tokens.
	tokens: usize,

	/// # Any Non-Greedy Matches?
	nongreedy: bool,

	/// # Code Size.
	code_size: CodeSize,
}

impl PartialOptimizationState {
	/// # New.
	const fn new(code_size: CodeSize) -> Self {
		Self {
			bits: 0,
			bytes: 0,
			tokens: 0,
			nongreedy: false,
			code_size,
		}
	}

	/// # "True" Bits.
	const fn true_bits(&self, bump: bool) -> usize {
		// If this were the last block, would the code size increase?
		let code_size =
			if bump && let Some(next) = self.code_size.next() { next }
			else { self.code_size };

		self.bits + (code_size as usize)
	}

	#[must_use]
	/// # Tokens Full?
	const fn tokens_full(&self) -> bool {
		MAX_TOKENS.get() <= self.tokens
	}
}



/// # Calculate Min Code Size.
///
/// Find and return the maximum number of bits required to represent the
/// slice data.
const fn min_code_size(mut src: &[u8]) -> CodeSize {
	// Find the highest index byte.
	let mut max_byte = 0;
	while let [ next, rest @ .. ] = src {
		if max_byte < *next {
			max_byte = *next;
			if 127 < max_byte { break; }
		}
		src = rest;
	}

	// Bytes start at zero, so minimum palette length is +1.
	let min_palette_len = (max_byte as u32) + 1;

	// Count the bits of the nearest power of two.
	match min_palette_len.next_power_of_two().trailing_zeros() {
		0..=2 => CodeSize::N02, // GIF requires a minimum of two.
		3 => CodeSize::N03,
		4 => CodeSize::N04,
		5 => CodeSize::N05,
		6 => CodeSize::N06,
		7 => CodeSize::N07,
		_ => CodeSize::N08,     // A byte won't ever need more!
	}
}
