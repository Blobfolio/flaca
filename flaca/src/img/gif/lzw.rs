/// # Helper: Count.
macro_rules! count {
	() => ( 0 );
	($odd:tt) => ( 1 );
	($odd:tt $( $a:tt $b:tt )+) => ( (count!($($a)+) * 2) + 1 );
	($( $a:tt $b:tt )+) =>		 (  count!($($a)+) * 2	  );
}

/// # Helper: Num Enum.
macro_rules! numenum {
	( #[repr($ty:ty)] $( #[doc = $doc:expr] )* $enum:ident $( $k:ident $v:literal, )+ ) => (
		#[repr($ty)]
		#[derive(Debug, Clone, Copy, Eq, Ord, PartialEq, PartialOrd)]
		$( #[doc = $doc] )*
		enum $enum {
			$( $k = $v, )+
		}

		/// # Sanity Checks.
		const _: () = {
			let mut all = $enum::ALL.as_slice();
			while let [ next, rest @ .. ] = all {
				assert!(
					rest.is_empty() || (*next as $ty) + 1 == (rest[0] as $ty),
					"BUG: enum is non-sequential.",
				);
				assert!(
					rest.is_empty() || (rest[0].prev().unwrap() as $ty) == (*next as $ty),
					"BUG: enum.next() is wrong.",
				);
				assert!(
					rest.is_empty() || (next.next().unwrap() as $ty) == (rest[0] as $ty),
					"BUG: enum.next() is wrong.",
				);
				all = rest;
			}
		};

		#[allow(
			clippy::allow_attributes,
			dead_code,
			reason = "Macro made me do it.",
		)]
		impl $enum {
			/// # All.
			const ALL: [Self; count!($( $k )+)] = [ $( Self::$k, )+ ];

			/// # Min.
			const MIN: Self = Self::ALL[0];

			/// # Max.
			const MAX: Self = Self::ALL[Self::ALL.len() - 1];

			/// # Previous.
			const fn prev(self) -> Option<Self> {
				let idx = self as usize - Self::MIN as usize;
				if 0 == idx { None }
				else { Some(Self::ALL[idx - 1]) }
			}

			/// # Next.
			const fn next(self) -> Option<Self> {
				let idx = self as usize - Self::MIN as usize;
				if idx + 1 < Self::ALL.len() { Some(Self::ALL[idx + 1]) }
				else { None }
			}

			/// # Is Max?
			const fn is_max(self) -> bool { matches!(self, Self::MAX) }

			/// # Saturating From.
			const fn saturating_from(v: $ty) -> Self {
				match v {
					$( $v => Self::$k, )+
					_ => Self::MAX,
				}
			}
		}
	);
}

